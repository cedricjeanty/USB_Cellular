#pragma once
// AirBridge Display HAL — IDisplay interface + shared rendering code
// Virtual: init(), flush(), ok() — hardware-specific
// Shared:  clear(), pixel(), text(), rect(), hline() — framebuffer math

#include <cstdint>
#include <cstring>

#define SCREEN_W   128
#define SCREEN_H    64
#define FRAMEBUF_SIZE (SCREEN_W * SCREEN_H / 8)  // 1024

class IDisplay {
public:
    virtual ~IDisplay() = default;

    // Hardware init. Returns true on success.
    virtual bool init() = 0;

    // Push framebuf to physical display (or capture in test).
    virtual void flush() = 0;

    // Is the display operational?
    virtual bool ok() const = 0;

    // Raw framebuffer — shared rendering code writes here directly
    uint8_t framebuf[FRAMEBUF_SIZE];

    // ── Shared rendering (NOT virtual — identical on all platforms) ──────

    void clear() { memset(framebuf, 0, FRAMEBUF_SIZE); }

    void pixel(int x, int y, bool on) {
        if (x < 0 || x >= SCREEN_W || y < 0 || y >= SCREEN_H) return;
        if (on) framebuf[x + (y / 8) * SCREEN_W] |=  (1 << (y & 7));
        else    framebuf[x + (y / 8) * SCREEN_W] &= ~(1 << (y & 7));
    }

    void hline(int x0, int x1, int y) {
        for (int x = x0; x <= x1; x++) pixel(x, y, true);
    }

    void rect(int x, int y, int w, int h, bool fill) {
        if (fill) {
            for (int j = y; j < y + h; j++)
                for (int i = x; i < x + w; i++)
                    pixel(i, j, true);
        } else {
            hline(x, x + w - 1, y);
            hline(x, x + w - 1, y + h - 1);
            for (int j = y; j < y + h; j++) {
                pixel(x, j, true);
                pixel(x + w - 1, j, true);
            }
        }
    }

    inline void draw_char(int x, int y, char c, int size);
    inline void text(int x, int y, const char* str, int size = 1);
    inline int  text_width(const char* str, int size = 1);
};

