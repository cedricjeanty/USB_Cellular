// ── Inline method implementations ───────────────────────────────────────────

inline void IDisplay::draw_char(int x, int y, char c, int size) {
    if (c < 32 || c > 126) c = '?';
    const uint8_t* glyph = &detail::font5x7[(c - 32) * 5];
    for (int col = 0; col < 5; col++) {
        uint8_t line = glyph[col];
        for (int row = 0; row < 7; row++) {
            if (line & (1 << row)) {
                if (size == 1) pixel(x + col, y + row, true);
                else           rect(x + col * size, y + row * size, size, size, true);
            }
        }
    }
}

inline void IDisplay::text(int x, int y, const char* str, int size) {
    int cx = x;
    while (*str) {
        draw_char(cx, y, *str, size);
        cx += 6 * size;
        str++;
    }
}

inline int IDisplay::text_width(const char* str, int size) {
    int len = strlen(str);
    return len > 0 ? len * 6 * size - size : 0;
}
